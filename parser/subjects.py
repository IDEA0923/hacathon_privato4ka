"""Маппинг русских названий предметов в однобуквенные коды.

Коды:
M - математика, I - информатика, P - физика, C - химия, B - биология,
R - русский язык, L - литература, H - история, S - обществознание,
E - английский (иностранные), A - астрономия, D - география,
T - технология/инженерия, G - геометрия (если встречается отдельно).
"""

import re

_KEYWORDS: list[tuple[str, str]] = [
    # порядок важен: более специфичные раньше
    ("информатик", "I"),
    ("программирован", "I"),
    ("математик", "M"),
    ("геометри", "G"),
    ("физик", "P"),
    ("хими", "C"),
    ("биолог", "B"),
    ("эколог", "B"),
    ("русск", "R"),
    ("литератур", "L"),
    ("истори", "H"),
    ("обществ", "S"),
    ("право", "S"),
    ("эконом", "S"),
    ("английск", "E"),
    ("немецк", "E"),
    ("французск", "E"),
    ("испанск", "E"),
    ("китайск", "E"),
    ("иностранн", "E"),
    ("лингвист", "E"),
    ("астроном", "A"),
    ("географ", "D"),
    ("технолог", "T"),
    ("инженер", "T"),
    ("робототехник", "T"),
]


def detect_subjects(text: str) -> str:
    """Возвращает строку из уникальных кодов, например 'MIP'.

    Если ничего не распозналось — возвращает пустую строку.
    Длина результата ограничена 20 символами (VARCHAR(20) в БД).
    """
    if not text:
        return ""
    low = text.lower()
    seen: list[str] = []
    for key, code in _KEYWORDS:
        if code in seen:
            continue
        if key in low:
            seen.append(code)
    return "".join(seen)[:20]


_CLEAN_RE = re.compile(r"\s+")


def normalize(text: str) -> str:
    return _CLEAN_RE.sub(" ", text or "").strip()
